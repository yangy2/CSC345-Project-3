// Parsed command representation
#define EXEC  1
#define REDIR 2
#define PIPE  3
#define LIST  4
#define BACK  5

#define MAXARGS 10

char *history[100];

char path[100] = "$/";
int current_path_idx = 1;

struct cmd {
  int type;
};

struct execcmd {
  int type;
  char *argv[MAXARGS];
  char *eargv[MAXARGS];
};

struct redircmd {
  int type;
  struct cmd *cmd;
  char *file;
  char *efile;
  int mode;
  int fd;
};

struct pipecmd {
  int type;
  struct cmd *left;
  struct cmd *right;
};

struct listcmd {
  int type;
  struct cmd *left;
  struct cmd *right;
};

struct backcmd {
  int type;
  struct cmd *cmd;
};

int fork1(void);  // Fork but panics on failure.
void panic(char*);
struct cmd *parsecmd(char*);

// Execute cmd.  Never returns.
void
runcmd(struct cmd *cmd)
{
  int p[2];
  struct backcmd *bcmd;
  struct execcmd *ecmd;
  struct listcmd *lcmd;
  struct pipecmd *pcmd;
  struct redircmd *rcmd;

  if(cmd == 0)
    exit();

  switch(cmd->type){
  default:
    panic("runcmd");

  case EXEC:
    ecmd = (struct execcmd*)cmd;
    if(ecmd->argv[0] == 0)
      exit();

    exec(ecmd->argv[0], ecmd->argv);
    printf(2, "exec %s failed\n", ecmd->argv[0]);
    break;

  case REDIR:
    rcmd = (struct redircmd*)cmd;
    close(rcmd->fd);
    if(open(rcmd->file, rcmd->mode) < 0){
      printf(2, "open %s failed\n", rcmd->file);
      exit();
    }
    runcmd(rcmd->cmd);
    break;

  case LIST:
    lcmd = (struct listcmd*)cmd;
    if(fork1() == 0)
      runcmd(lcmd->left);
    wait();
    runcmd(lcmd->right);
    break;

  case PIPE:
    pcmd = (struct pipecmd*)cmd;
    if(pipe(p) < 0)
      panic("pipe");
    if(fork1() == 0){
      close(1);
      dup(p[1]);
      close(p[0]);
      close(p[1]);
      runcmd(pcmd->left);
    }
    if(fork1() == 0){
      close(0);
      dup(p[0]);
      close(p[0]);
      close(p[1]);
      runcmd(pcmd->right);
    }
    close(p[0]);
    close(p[1]);
    wait();
    wait();
    break;

  case BACK:
    bcmd = (struct backcmd*)cmd;
    if(fork1() == 0)
      runcmd(bcmd->cmd);
    break;
  }
  exit();
}

int
getcmd(char *buf, int nbuf)
{
  printf(2, "%s ", path);
  memset(buf, 0, nbuf);
  gets(buf, nbuf);
  if(buf[0] == 0) // EOF
    return -1;
  return 0;
}

void print_history(int arr_len) {
  printf(2, "output: \n");
  int i = 0;
  for(; i < arr_len; i++) {
    if(history[i] == NULL)
      break;
  }
  if(i <= 10)
    i = 0;
  else
    i -= 10;
  for(; i < arr_len; i++) {
    if(history[i] != NULL) {
      printf(2, "  %d\t", i+1);
      printf(2, history[i], "\n");
    }
    else
      break;
  }
}

int execute_prev(int index, int arr_len, int should_wait) {
  if(fork1() == 0) {
    if(strcmp(history[index], "history\n") == 0) {
      print_history(arr_len);
      return 1;
    }
    runcmd(parsecmd(history[index]));
  }
  if(should_wait)
    wait();
  return 1;
}

/*
  Performs the last command in the history buffer.
*/
void execute_last(int arr_len, int should_wait) {
  int i = 0;
  for(; i < arr_len; i++) {
    if(history[i] == NULL) {
      i--;
      break;
    }
  }
  execute_prev(i, arr_len, should_wait);
}

/*
  Determines whether the process will be a background process, based on
  whether the user input "&" after the command.
*/
int should_wait(char buf[], int buf_len) {
  int i = 0;
  for(; i < buf_len; i++) {
    if(buf[i] == '\0') {
      break;
    }
  }
  if(buf[i-1] == '&')
    return 0;
  return 1;
}

int
main(void)
{
  static char buf[100];
  int fd;
  int command_count; // tracks number of commands entered at console

  // Assumes three file descriptors open.
  while((fd = open("console", O_RDWR)) >= 0){
    if(fd >= 3){
      close(fd);
      break;
    }
  }

  // Read and run input commands.
  while(getcmd(buf, sizeof(buf)) >= 0){
    int buf_len = strlen(buf);
    int waiter = should_wait(buf, buf_len);
    int arr_len = sizeof(history)/sizeof(history[0]);

    if(buf[0] == '!') {
      if(buf[1] == '!') {
        execute_last(arr_len, waiter);
        continue;
      }
      else {
        int num = atoi(buf+1);
        execute_prev(num-1, arr_len, waiter);
        continue;
      }
    }

    history[command_count] = malloc(sizeof(char)*(buf_len+1));
    strcpy(history[command_count], buf);
    command_count++;

    if(buf[0] == 'p' && buf[1] == 's') {
      buf[strlen(buf)-1] = 0;
      if(ps() < 0)
        printf(2, "Did not work.");
      continue;
    }

    if(buf[0] == 'c' && buf[1] == 'd' && buf[2] == ' '){
      // Clumsy but will have to do for now.
      // Chdir has no effect on the parent if run in the child.
      int path_len = sizeof(path)/sizeof(path[0]);
      buf[strlen(buf)-1] = 0;  // chop \n
      if(chdir(buf+3) < 0)
        printf(2, "cannot cd %s\n", buf+3);

      else if (buf[3] == '.' && buf[4] == '.') {
        int last_slash_idx = 1;
        int i = 1;
        int true_path_len = 0;
        for(; true_path_len < path_len; true_path_len++) {
          if(path[true_path_len] == '\0') {
            true_path_len--;
            break;
          }
        }
        for(; i < true_path_len-1; i++) {
          if(path[i] == '/')
            last_slash_idx = i;
        }
        //printf(2, "%d\n", last_slash_idx);
        path[last_slash_idx+1] = '\0';
        for(; last_slash_idx < true_path_len; last_slash_idx++)
          current_path_idx--;
      }
      else {
        for(int i = 3; i < path_len; i++) {
          current_path_idx++;
          if(buf[i] == '\0') {
            path[current_path_idx] = '/';
            break;
          }
          path[current_path_idx] = buf[i];
        }
      }
      continue;
    }

    if(strcmp(buf, "history\n") == 0) {
      print_history(arr_len);
      continue;
    }
    if(fork1() == 0)
      runcmd(parsecmd(buf));
    if(waiter)
      wait();
  }
  exit();
}

void
panic(char *s)
{
  printf(2, "%s\n", s);
  exit();
}

int
fork1(void)
{
  int pid;

  pid = fork();
  if(pid == -1)
    panic("fork");
  return pid;
}

